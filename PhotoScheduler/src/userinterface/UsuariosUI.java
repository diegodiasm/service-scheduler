    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (getTbUsuarios().getSelectedRowCount()>0) {

            Controller ctrl = Controller.getInstance();
            int line = Math.max(getTbUsuarios().getSelectedRow(), 1);
            
            ctrl.frUsuariosUIBtExcluir(this);
            ctrl.frUsuariosUIRefreshTable(this);
            if (getTbUsuarios().getRowCount()>0) {
               getTbUsuarios().requestFocus();
               getTbUsuarios().setRowSelectionInterval(line-1,line-1);
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

