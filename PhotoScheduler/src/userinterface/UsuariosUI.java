    public int getSelecedUserId() {
        
        int userId;
        int row = getTbUsuarios().getSelectedRow();

        userId = Integer.parseInt(getUsuariosTableModel().
                getValueAt(row, getUsuariosTableModel().findColumn("ID")).
                toString());

        return userId;

    }

    public Usuarios getFormData() {
        
        Usuarios usuario = new Usuarios();
        String permissao = "";

        permissao+=chbAcompanhar.isSelected()?"A":"";
        permissao+=chbCadastrar.isSelected()?"C":"";
        permissao+=chbConfigurar.isSelected()?"F":"";
        permissao+=getChbAgendamentoPrioritario().isSelected()?"P":"";
        permissao+=getChbAtenderExcluir().isSelected()?"E":"";
        
        usuario.setName(tfNomeUsuario.getText());
        usuario.setUsername(tfLogin.getText());
        usuario.setPassword(String.valueOf(tfSenha.getPassword()));
        usuario.setPermissao(permissao);
        
        return usuario;
    }
    
    public void cleanFields() {
        
        tfLogin.setText("");
        tfSenha.setText("");
        tfNomeUsuario.setText("");
        chbAcompanhar.setSelected(false);
        chbConfigurar.setSelected(false);
        chbCadastrar.setSelected(false);
        
    }

